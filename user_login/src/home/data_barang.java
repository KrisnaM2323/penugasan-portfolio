    private void AmbilKondisi(String kondisi) {
        String[]kds = {"Baik", "Jelek", "Rusak", "Rusak Parah"};
        cbkondisi.removeAllItems();
        cbkondisi.addItem(kondisi);
        for(int i = 0;i< kds.length;i++){
            if(kds[1].equals(kondisi)){
                cbkondisi.addItem(kds[i]);
            }
        }
    }
    
