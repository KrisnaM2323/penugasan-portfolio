    private void cbrefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbrefreshActionPerformed
        // TODO add your handling code here:
        try {
            conn = koneksi.getKoneksi();
            Statement stt = conn.createStatement();
            String sql="select * from inputdata order by kode_barang desc";
            ResultSet rs = stt.executeQuery(sql);
            if (rs.next()){
                String no_brg = rs.getString("kode_barang").substring(3);
                String AN = "" + (Integer.parseInt(no_brg) + 1);
                String Nol = "";
                
                if(AN.length()==1){
                    Nol = "000";
                } else if(AN.length()==2){
                    Nol = "00";
                } else if(AN.length()==3){
                    Nol = "0";
                } else if(AN.length()==4){
                    Nol = "";
                }
                
                txkode_brg.setText("BRG" + Nol + AN);
                  
            } else {
                txkode_brg.setText("BRG0001");
            }
//            rs.close();
//            conn.close();
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
//        txkode_brg.setText("");
        txnm_barang.setText("");
//        cbkondisi.setSelectedItem("");
//        cbkondisi.getSelectedItem();
        txjumlah.setText("");
        
        
        data_barang file=new data_barang();
        file.setVisible(true);
        
        this.dispose();
        
    }//GEN-LAST:event_cbrefreshActionPerformed

